//Con Parametro
function suma(pnumeroUno, pnumeroDos) {
    let numeroUno = pnumeroUno
    let numeroDos = pnumeroDos
    let sumar = numeroUno + numeroDos
    return sumar + " Parametro";
}

function resta(pnumeroUno, pnumeroDos) {
    let numeroUno = pnumeroUno
    let numeroDos = pnumeroDos
    let restar = numeroUno - numeroDos
    return restar + " Parametro";
}

function multiplicacion(pnumeroUno, pnumeroDos) {
    let numeroUno = pnumeroUno
    let numeroDos = pnumeroDos
    let multiplicar = numeroUno * numeroDos
    return multiplicar + " Parametro";
}

function division(pnumeroUno, pnumeroDos) {
    let numeroUno = pnumeroUno
    let numeroDos = pnumeroDos
    let dividir = numeroUno / numeroDos
    return dividir + " Parametro";
}

