function suma($numeroUno, $numeroDos){
    $resultado= $numeroUno + $numeroDos;
    return $resultado;
}

function resta($numeroUno, $numeroDos){
    $resultado= $numeroUno - $numeroDos;
    return $resultado;
}

function multiplicacion($numeroUno, $numeroDos){
    $resultado= $numeroUno * $numeroDos;
    return $resultado;
}

function division($numeroUno, $numeroDos){
    $resultado= $numeroUno / $numeroDos;
    return $resultado;
}
